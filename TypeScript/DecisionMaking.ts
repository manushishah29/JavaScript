//if statement
var a=10;
if(a>10)
{
    console.log("Grater than 10");
}

//if-else statment
var a=19
if(a>20)
{
    console.log("Grater than 20");
}
else
{
    console.log("Less than 20");
}

//Switch Statement

var Grade="A";

switch(Grade)
{
    case "A":
        console.log("Exellent");
        break;
    case "B":
        console.log("Average");
        break;
    default:
        console.log("Bad");
}